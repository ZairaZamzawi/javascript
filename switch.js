function testingSwitch() {
  var nilai = 50;

  // Sama seperti select case vb
  switch (nilai) {
    case 30:
      console.log("Ini 30 dong nilainya");
      break;
    case 20:
      console.log("Ini 20 dong nilainya");
      break;
    case 50:
      console.log("Ini 50 dong nilainya");
      break;
    // case else di vb
    default:
      console.log("Ga bener nih ngasih nilainya");
  }

}

testingSwitch()
